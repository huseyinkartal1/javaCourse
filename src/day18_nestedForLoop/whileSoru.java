package day18_nestedForLoop;

public class whileSoru {
    public static void main(String[] args) {

        int a=10;
        int b=15;


        for (int i=a; i<=b;i++){

            System.out.print(i+" ");
        }

        System.out.println();
        while(a<=b){
            System.out.print(a+" ");

            a+=1;
        }




    }
}
